#include <iostream>
using namespace std;

int main()
{
    int n;
    cin >> n;
    for (int i = 1; i <= n; i++)
    {
        if (i == 3)
        {
            cout << "X"
                 << " ";
        }
        else if (i == 6)
        {
            cout << "X"
                 << " ";
        }
        else if (i == 9)
        {
            cout << "X"
                 << " ";
        }
        else
        {
            cout << i << " ";
        }
    }
}